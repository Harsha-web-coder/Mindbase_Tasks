//simple program that demonstrates the basic math operations in JavaScript
// Addition
const sum = 5 + 3;
console.log("Sum:", sum); // Output: Sum: 8

// Subtraction
const difference = 10 - 4;
console.log("Difference:", difference); // Output: Difference: 6

// Multiplication
const product = 2 * 6;
console.log("Product:", product); // Output: Product: 12

// Division
const quotient = 15 / 3;
console.log("Quotient:", quotient); // Output: Quotient: 5

// Remainder (Modulo)
const remainder = 17 % 5;
console.log("Remainder:", remainder); // Output: Remainder: 2

// Exponentiation
const power = 2 ** 3;
console.log("Power:", power); // Output: Power: 8

// Increment
let number = 5;
number++;
console.log("Increment:", number); // Output: Increment: 6

// Decrement
number--;
console.log("Decrement:", number); // Output: Decrement: 5
